# echo -e 是 Bash 中用于启用反斜杠转义字符解释的命令选项。
# 当使用 -e 选项时，echo 会解释字符串中的特殊转义序列，而不是直接输出它们。

echo $(pwd)   # 获取命令的输出
echo $(ls)    # 获取命令的输出
echo $(date)  # 获取命令的输出

# 1. 基本转义字符
echo -e "第一行\n第二行"
# 输出：
# 第一行
# 第二行

echo -e "姓名:\t张三\t年龄:\t25"
# 输出：姓名:    张三    年龄:    25

# 2. 特殊效果
# 进度条效果
echo -ne '进度: [#####     ] 50%\r'
sleep 1
echo -ne '进度: [##########] 100%\n'

# 警报声（可能不适用于所有终端）
echo -e "\a注意！操作完成"

# 3. 颜色输出
# 红色文本
echo -e "\033[31m这是红色文字\033[0m"

# 绿色背景，白色文字
echo -e "\033[42;37m绿底白字\033[0m"

# 闪烁文本
echo -e "\033[5;31m闪烁的红色警告\033[0m"



